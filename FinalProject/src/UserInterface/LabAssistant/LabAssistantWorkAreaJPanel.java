    private void processActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processActionPerformed

       int selectedRow = labWorkReq.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select atleast one row");
            return;
        }

        TestRequest request = (TestRequest)labWorkReq.getValueAt(selectedRow, 0);

        
        
        StringBuffer selectedList =request.getType();
        String[] selected=selectedList.toString().split(",");
          
        ProcessRequestJPanel processRequestJPanel = new ProcessRequestJPanel(selected,request,userProcessContainer);
        userProcessContainer.add("processRequestJPanel", processRequestJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
     


    }//GEN-LAST:event_processActionPerformed

