    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(jRadioButton1.isSelected()){
            BloodBankmember b=new BloodBankmember();
            b.setFirstName(userAccount.getPatient().getFirstName());
            b.setMedicalId(userAccount.getPatient().getMedicalRecord().getMedicalId());
            b.setUseraccount(userAccount);
            enterprise.getBloodbankmembersdirectory().addbloodbankmember(b);
            
        }
        else{
            
          for(BloodBankmember bb:enterprise.getBloodbankmembersdirectory().getBloodbankdirectory()){
              if(bb.getUseraccount().equals(userAccount))
            enterprise.getBloodbankmembersdirectory().deletebloodbankmember(bb);
            
        }}
    }//GEN-LAST:event_jButton1ActionPerformed

