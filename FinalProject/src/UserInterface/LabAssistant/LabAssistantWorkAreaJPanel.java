    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)labWorkReq.getModel();
           //SimpleDateFormat formatter = new SimpleDateFormat("dd-MMM-yyyy");
       model.setRowCount(0);
        
      for(WorkRequest request : organization.getWorkQueue().getWorkRequestList()){
            if(request instanceof TestRequest)
            {
                String value =null;
                Object[] row = new Object[7];
               row[0] = ((TestRequest) request);
               row[1] =  ((TestRequest) request).getSender();
               row[2] = ((TestRequest) request).getType();
               row[3] = ((TestRequest) request).getAdditionalTests();
               row[4] = ((TestRequest) request).getComments();
               row[5] = ((TestRequest) request).getRequestDate();
               row[6] = ((TestRequest) request).getMedicalId();
             

                model.addRow(row);
            }
        } 
    }
